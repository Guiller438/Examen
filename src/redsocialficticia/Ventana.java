    private void btn_aniadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_aniadirActionPerformed
        Persona person = new Persona(Integer.parseInt(txt_id.getText()),
                                     txt_nombre.getText(), 
                                     Integer.parseInt(txt_edad.getText()),
                                     Integer.parseInt(txt_prioridad.getText()),
                                      Integer.parseInt(txt_amigos.getText()));
        if(EsperaActivos.agregarPersona(person)){
            EsperaActivos.encolar(person);
            JOptionPane.showMessageDialog(null, "Se ha registrado de manera exitosa");
        }else{
            JOptionPane.showMessageDialog(null, "Ya se encuentra registrado ese nombre");
        }
                      
    }//GEN-LAST:event_btn_aniadirActionPerformed

    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        if(cbo_colas.getSelectedIndex() == 0){
            if(Activos.buscar(Integer.parseInt(txt_buscarid.getText())) == null){
            JOptionPane.showMessageDialog(null, "NO SE ENCONTRO");
            }else{
            area_buscados.setText((Activos.buscar(Integer.parseInt(txt_buscarid.getText()))).toString());
            }
        }else{
            if(EsperaActivos.buscar(Integer.parseInt(txt_buscarid.getText())) == null){
            JOptionPane.showMessageDialog(null, "NO SE ENCONTRO");
            }else{
            area_buscados.setText((EsperaActivos.buscar(Integer.parseInt(txt_buscarid.getText()))).toString());
        }
        }
        
       
    }//GEN-LAST:event_btn_buscarActionPerformed

